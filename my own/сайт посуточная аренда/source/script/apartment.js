function turnPrevSlide(e) {
	const sliderImage = e.target
		.closest(".apartmentSliderWrapper")
		.querySelector(".slideContainer img");
	const slideList = e.target.closest(".apartmentSliderWrapper").firstElementChild
		.children;
	let currentSlide;
	for (let i = 0; i < slideList.length; i++) {
		slideList[i].classList.remove("_active");
		if (slideList[i].firstElementChild.src == sliderImage.src) {
			currentSlide = slideList[i - 1];
			if (currentSlide == slideList[-1]) {
				currentSlide = slideList[slideList.length - 1];
			}
		}
	}
	currentSlide.classList.add("_active");
	sliderImage.src = currentSlide.firstElementChild.src;
	setBackground(e, currentSlide);
}

function turnNextSlide(e) {
	const sliderImage = e.target
		.closest(".apartmentSliderWrapper")
		.querySelector(".slideContainer img");
	const slideList = e.target.closest(".apartmentSliderWrapper").firstElementChild
		.children;
	let currentSlide;
	for (let i = 0; i < slideList.length; i++) {
		slideList[i].classList.remove("_active");
		if (slideList[i].firstElementChild.src == sliderImage.src) {
			currentSlide = slideList[i + 1];
			if (currentSlide == slideList[slideList.length]) {
				currentSlide = slideList[0];
			}
		}
	}
	currentSlide.classList.add("_active");
	sliderImage.src = currentSlide.firstElementChild.src;
	setBackground(e, currentSlide);
}

