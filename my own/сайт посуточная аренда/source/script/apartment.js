function chooseTheSlide(e) {
	if (e.target.tagName == "IMG") {
		const decreasedSlideArray = e.target
			.closest(".decreasedSlides")
			.querySelectorAll(".decreasedSlide");
		decreasedSlideArray.forEach((decreasedSlide) => {
			decreasedSlide.classList.remove("_active");
		});
		e.target.closest(".decreasedSlide").classList.add("_active");
		const sliderImage = e.target
			.closest(".apartmentSliderWrapper")
			.querySelector(".slideContainer img");
		sliderImage.src = e.target.src;
		setBackground(e);
	}
}

